from AbstractTuringMachine import *
from collections import defaultdict
import random
import string



class TuringMachine(AbstractTuringMachine):

    @property
    def ListStanow(self):
        return list(self.states)

    @property
    def Przejscia(self):
        return self.transitions

    #######################################

    def WykonajKrok(self, ZnakZTasmy):
        if self.halted:
            raise RuntimeError("Halted machine cannot step.")
        
        try:
            state, symbol, direction = self.transitions[(self.current_state, ZnakZTasmy)]
        except KeyError("Given combination of state and symbol is not valid."):
            self.halted = True
            return

        self.current_state = state
        self.tape[self.head] = symbol
        self.head += direction

    def Uruchom(self, Tasma):
        self.tape = defaultdict(lambda: self.blank_symbol, Tasma)
        while not self.CzyStanJestKoncowy(self.current_state) and not self.halted:
            self.show()
            self.WykonajKrok(self.tape[self.head])
        self.halted = True

    def CzyStanJestPoczatkowy(self, Stan):
        return Stan == self.initial_state

    def CzyStanJestKoncowy(self, Stan):
        return Stan in self.final_states
    
    #######################################

    def __init__(self, states, alphabet, blank_symbol, initial_state, final_states, transitions):
        
        #initialization
        self.states = states
        self.alphabet = alphabet
        self.blank_symbol = blank_symbol
        self.initial_state = initial_state
        self.final_states = final_states
        self.transitions = transitions  #state, symbol -> new state, new symbol, direction

        self.head = 0
        self.current_state = initial_state
        self.halted = False
        
        #correctivity check
        for key, value in self.transitions.items():
            if not (key[0] in self.states and value[0] in self.states):
                raise ValueError("Names of states are not consistent with respect to the diagram of transitions.")
            if not (key[1] in self.alphabet and value[1] in self.alphabet):
                raise ValueError("Descriptions of transitions between states must include symbols of alphabet.")
   
    
    def final_input(self):
        if not self.halted:
            raise RuntimeError("Machine still running.")
        return self.current_state in self.final_states

    def show(self, window=40):
        print("... ", end='')
        print(''.join(self.tape[i] for i in range(self.head - window, self.head + window + 1)), end='')
        print(f' ... state={self.current_state}')
        print(f'{" " * (window + 4)}^')

        with open('TM.txt', 'a') as file:
            file.write("... ")
            file.write(''.join(self.tape[i] for i in range(self.head - window, self.head + window + 1)))
            file.write(f' ... state={self.current_state}\n')
            file.write(f'{" " * (window + 4)}^\n')



def generate_nearly_random_ipv6():
    ipv6 = []
    for i in range(8):
        zero = random.choice((True, False))
        if zero:
            for j in range(4):
                ipv6.append('0')
        else:
            for j in range(4):
                ipv6.append(random.choice(list(string.hexdigits[:16])))
        if i < 7:
            ipv6.append(':')
    return ipv6
    




